package CommonClasses;

import java.util.Vector;

public class Cours {
    private int id;
    private Matiere matiere;
    private String intitule;
    private String nom;
    private Vector<Annee> anneeAuthorisee;
    private Format format;
    private String path;

    public Cours()
    {
        this.id = -1;
        this.matiere = null;
        this.intitule = null;
        this.nom = null;
        this.anneeAuthorisee = null;
        this.format = null;
        this.path = null;
    }

    public Cours(int id, String intitule, Matiere matiere, String nom, Format format, String path, Vector<Annee> anneeAuthorisee)
    {
        this.id = id;
        this.matiere = matiere;
        this.intitule = intitule;
        this.nom = nom;
        this.anneeAuthorisee = anneeAuthorisee;
        this.format = format;
        this.path = path;
    }

    public int getId(){return this.id;}
    public void setId(int id){this.id = id;}
    public Matiere getMatiere(){return this.matiere;}
    public void setMatiere(Matiere matiere){this.matiere = matiere;}
    public String getIntitule(){return this.intitule;}
    public void setIntitule(String intitule){this.intitule = intitule;}
    public String getNom(){return this.nom;}
    public void setNom(String nom){this.nom = nom;}
    public Vector<Annee> getAnneeAuthorisee(){return this.anneeAuthorisee;}
    public void setAnneeAuthorisee(Vector<Annee> anneeAutorisee){this.anneeAuthorisee = anneeAutorisee;}
    public Format getFormat(){return this.format;}
    public void setFormat(Format format){this.format = format;}
    public String getPath(){return this.path;}
    public void setPath(String path){this.path = path;}
}